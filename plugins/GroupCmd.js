// ╔══════════════════════════════════════╗
// ║      🛡️ قائمة أوامر الجروبات        ║
// ║  تُفتح عند الضغط على زر الجروبات    ║
// ╚══════════════════════════════════════╝

// ───────────────────────────────────────
// ⚙️  إعدادات
// ───────────────────────────────────────
const CONFIG = {
  image: global.imgBot || global.img || '',
  botName: global.botName || 'Bot',
}

// ───────────────────────────────────────
// 🚀 Handler
// ───────────────────────────────────────
let handler = async (m, { conn }) => {

  const body =
`╭━━━━━━━━━━━━━━━━━━━━╮
┃   🛡️ *أوامر الجروبات*
╰━━━━━━━━━━━━━━━━━━━━╯

اختر الأمر المطلوب من الأزرار أدناه 👇`

  const footer = `⚡ ${CONFIG.botName} • أوامر الجروبات`

  // ✏️ ======================================
  // أضف أوامر الجروبات هنا بنفس الصيغة:
  //   ['📢 نص_الزر', 'اسم_الأمر'],
  //
  // مثال:
  //   ['📢 رفع الكل', 'tagall'],
  //   ['🚫 كتم عضو',  'mute'],
  //   ['✅ فتح الكتم', 'unmute'],
  //   ['👑 ترقية عضو', 'promote'],
  // ========================================
  const buttons = [
    // ← أضف أزرارك هنا

    // زر مؤقت لحين إضافة الأوامر
    ['🔧 قريباً', 'soon'],
  ]

  await conn.sendButton2(
    m.chat,
    body,
    footer,
    CONFIG.image,
    buttons,
    null,
    null,
    m
  )
}

handler.help    = ['groupcmds']
handler.tags    = ['group']
handler.command = /^groupcmds$/i

export default handler