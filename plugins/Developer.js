// ╔══════════════════════════════════════╗
// ║       📞 جهة اتصال المطور            ║
// ║  تُرسل عند الضغط على زر "المطور"    ║
// ╚══════════════════════════════════════╝

// ───────────────────────────────────────
// ⚙️  بيانات المطور — عدّلها بمعلوماتك
// ───────────────────────────────────────
const DEV = {
  number: '201201609786', // ← رقم المطور بدون + (مثال: 201012345678)
  name:   'R∆yGumo',   // ← اسم المطور كما سيظهر في الكونتاكت
}

// ───────────────────────────────────────
// 🚀 Handler
// ───────────────────────────────────────
let handler = async (m, { conn }) => {

  // رسالة تمهيدية قبل إرسال الكونتاكت
  await conn.reply(
    m.chat,
    `📞 *جهة اتصال المطور*\n\nيمكنك التواصل معنا عبر الكونتاكت أدناه 👇`,
    m
  )

  // إرسال الكونتاكت كـ VCARD
  await conn.sendContact(
    m.chat,
    [[DEV.number, DEV.name]],
    m
  )
}

handler.help    = ['developer']
handler.tags    = ['main']
handler.command = /^developer$/i

export default handler